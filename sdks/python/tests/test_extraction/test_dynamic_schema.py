# ── (b1) §10-B1 token-budget soft cap + log on truncate ────────────────


def test_vocab_soft_cap_truncates_prompt_but_not_validation(caplog):
    import logging

    big = tuple(f"kind_{i}" for i in range(DEFAULT_VOCAB_SOFT_CAP + 10))
    schema = ExtractionSchema(entity_kinds=big, vocab_soft_cap=DEFAULT_VOCAB_SOFT_CAP,
                              label="big@v1")
    with caplog.at_level(logging.WARNING):
        rendered = schema.render_entity_kinds()
    assert len(rendered) == DEFAULT_VOCAB_SOFT_CAP  # prompt hint trimmed
    assert any("truncated" in r.message for r in caplog.records)  # logged
    # Validation still accepts a value BEYOND the cap (truncation = prompt only).
    dropped = []
    over_cap_kind = big[-1]  # past the soft cap
    out = _tolerant_parse_entities(
        [{"name": "Z", "kind": over_cap_kind, "confidence": 0.9}],
        on_dropped=lambda op, r: dropped.append(r), schema=schema,
    )
    assert [e.kind for e in out] == [over_cap_kind]
    assert dropped == []


def test_from_resolved_builds_projection_and_degrades_gracefully():
    resolved = {
        "entity_kinds": ["person", "cultivator", "person", "  ", 5],  # dedup + drop junk
        "edge_predicates": ["pursues"],
        "allow_free_edges": False,
        "label": "proj-1@v3",
    }
    sch = ExtractionSchema.from_resolved(resolved)
    assert sch.entity_kinds == ("person", "cultivator")  # deduped, junk dropped
    assert sch.edge_predicates == ("pursues",)
    assert sch.allow_free_edges is False
    assert sch.label == "proj-1@v3"
    # Missing keys → empty (more permissive, never stricter).
    assert sch.event_kinds == ()
    assert sch.fact_types == ()
