"""Lane LB — golden-set / backward-compat proof for dynamic extraction schema.

KG customizable-ontology epic (docs/specs/2026-06-20-...): the extraction SDK
ontology (entity/event/fact kinds + edge predicates) moves from a hardcoded
``Literal`` + ``.md`` vocab to a schema PASSED IN at call time. This is the
HIGHEST-RISK lane: the SDK is consumed by knowledge-service, worker-ai, AND
translation-service. The NON-NEGOTIABLE rule: ``schema=None`` reproduces today's
EXACT static behavior (byte-identical prompt + same Literal validation) so the
other two services are byte-for-byte unaffected.

This file proves backward-compat DETERMINISTICALLY (no LLM calls):

  1. Snapshot test — schema=None prompt == the pre-change static prompt
     (hashes captured from the pre-LB code; this test FAILS if the default
     path is altered).
  2. Validation parity — schema=None accepted kind/type sets == today's
     Literal members exactly.
  3. Dynamic path — schema=<xianxia-harem> injects the schema's
     edges/drives/kinds into the prompt + validation accepts them + rejects
     off-schema values fail-soft (collect via on_dropped, never crash).
"""

