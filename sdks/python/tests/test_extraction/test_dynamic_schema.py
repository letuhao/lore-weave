# ── (3) dynamic path: xianxia-harem schema ─────────────────────────────

XIANXIA_HAREM = ExtractionSchema(
    entity_kinds=("person", "cultivator", "sect", "spirit_beast", "dao_companion"),
    edge_predicates=("pursues", "disciple_of", "dao_companion_of", "rival_of", "seeks"),
    event_kinds=("action", "breakthrough", "tribulation", "courtship"),
    fact_types=("description", "realm", "affection"),
    allow_free_edges=False,  # CLOSED edge set
    label="xianxia-harem@v1",
)


@pytest.mark.parametrize("op,needles", [
    ("entity", ["cultivator", "dao_companion"]),
    ("relation", ["pursues", "dao_companion_of"]),
    ("event", ["breakthrough", "tribulation"]),
    ("fact", ["realm", "affection"]),
])
def test_dynamic_prompt_injects_schema_vocab(op, needles):
    built = _BUILDERS[op](_KNOWN, schema=XIANXIA_HAREM)
    assert "Project ontology (custom schema)" in built
    for n in needles:
        assert n in built, f"{op}: schema vocab {n!r} not injected into prompt"
    # Still byte-DIFFERENT from the static prompt (the dynamic block was added).
    assert _sha(built) != _BASELINE_SHA256[op]


def test_dynamic_relation_closed_set_note_present():
    built = relation.build_relation_system(_KNOWN, schema=XIANXIA_HAREM)
    assert "CLOSED" in built  # allow_free_edges=False states the closed rule


def test_dynamic_relation_free_edges_note_when_open():
    open_schema = ExtractionSchema(
        edge_predicates=("pursues", "seeks"), allow_free_edges=True,
    )
    built = relation.build_relation_system(_KNOWN, schema=open_schema)
    assert "may coin" in built.lower()
    assert "CLOSED" not in built


