# ── Baseline hashes captured from the PRE-LB static builders ──────────
# Generated by running the pre-change `build_<op>_system(["Kai","Harbin"])`
# on the parent branch and hashing the result. If the schema=None default
# path ever drifts byte-for-byte, these hashes mismatch → test FAILS, which
# is exactly the backward-compat tripwire worker-ai + translation rely on.
_KNOWN = ["Kai", "Harbin"]
_BASELINE_SHA256 = {
    "entity": "b4df60706db908ad5a1ea6564818e04c5e6ec63b186cdae3d10973d48878e954",
    "relation": "942544041a4c82b12d28aaa673ccbb762046d8c984e7e4c45df655fc4aed6e73",
    "event": "56a475dff1ab7cf7ea19e247265dc6319a50dfc957b24b289409d1061286fc02",
    "fact": "900a52ba19238f42332d3a8652888d638d6f466ddc93fbc732ff546ff67b9fd0",
}

_BUILDERS = {
    "entity": entity.build_entity_system,
    "relation": relation.build_relation_system,
    "event": event.build_event_system,
    "fact": fact.build_fact_system,
}


def _sha(s: str) -> str:
    return hashlib.sha256(s.encode("utf-8")).hexdigest()


# ── (1) snapshot: schema=None is byte-identical to the static .md prompt ──


@pytest.mark.parametrize("op", ["entity", "relation", "event", "fact"])
def test_schema_none_prompt_byte_identical_to_static(op):
    built = _BUILDERS[op](_KNOWN)  # schema defaults to None
    assert _sha(built) == _BASELINE_SHA256[op], (
        f"{op}: schema=None prompt drifted from the pre-LB static .md prompt — "
        "backward-compat BROKEN (worker-ai + translation get different output)"
    )


@pytest.mark.parametrize("op", ["entity", "relation", "event", "fact"])
def test_schema_none_explicit_equals_default(op):
    """Passing schema=None explicitly == the no-schema default (no path split)."""
    assert _BUILDERS[op](_KNOWN) == _BUILDERS[op](_KNOWN, schema=None)


@pytest.mark.parametrize("op", ["entity", "relation", "event", "fact"])
def test_empty_schema_appends_nothing(op):
    """A degenerate schema (no projected vocab for the op) appends NOTHING —
    still byte-identical to the static prompt (the dynamic block only renders
    when there's vocab)."""
    empty = ExtractionSchema()  # all vocab tuples empty
    assert _sha(_BUILDERS[op](_KNOWN, schema=empty)) == _BASELINE_SHA256[op]


# ── (2) validation parity: schema=None accepts exactly today's Literal set ──


def _job_items(items):
    """Minimal stand-in: the tolerant parsers take the raw list directly."""
    return items


def test_entity_validation_parity_static():
    # Every static kind accepted; an off-Literal kind dropped (schema=None).
    dropped = []
    items = [{"name": "X", "kind": k, "confidence": 0.9} for k in _STATIC_ENTITY_KINDS]
    items.append({"name": "Y", "kind": "cultivator", "confidence": 0.9})  # off-Literal
    out = _tolerant_parse_entities(items, on_dropped=lambda op, r: dropped.append(r))
    accepted_kinds = {e.kind for e in out}
    assert accepted_kinds == _STATIC_ENTITY_KINDS
    assert dropped == ["validation"]  # the cultivator dropped


