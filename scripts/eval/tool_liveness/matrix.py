"""Matrix reporter — per-tool G1–G4 → matrix.json + matrix.md + transcript.jsonl.

Row schema is CD3-frozen:
  {tool, service, tier, async, paid, probe, G1..G4, evidence, status, notes}

Persisted to docs/eval/tool-liveness/<date>/ (repo convention: store-reports-in-files).
"""
from __future__ import annotations

import json
from pathlib import Path

GATE_ORDER = ("G1", "G2", "G3", "G4")


def status_for(row: dict) -> str:
    if row.get("status") in ("UNTESTED-PAID", "WAIVED"):
        return row["status"]
    gates = [row.get(g) for g in GATE_ORDER]
    if all(g == "PASS" for g in gates):
        return "PASS"
    if any(g == "SKIP" for g in gates) and not any(g == "RED" for g in gates):
        return "PARTIAL"
    return "RED"


def write_matrix_json(rows: list[dict], path: Path) -> None:
    path.write_text(json.dumps(rows, ensure_ascii=False, indent=2), encoding="utf-8")


def _cell(v) -> str:
    return {"PASS": "✅", "RED": "❌", "SKIP": "—", None: "·"}.get(v, str(v))


def render_md(rows: list[dict], meta: dict) -> str:
    L: list[str] = []
    L.append(f"# Tool Liveness Eval — {meta.get('label','P0')} matrix")
    L.append("")
    L.append("> Generated by `scripts/eval/tool_liveness/`. Each row: a black-box NL ask was "
             "sent to a real model on a real stack; G1 the model **selected** the tool, G2 the "
             "**args** were schema-valid, G3 the call **executed** (Tier-W incl. confirm "
             "round-trip), G4 the effect was **verified via an independent DB read-back** "
             "(never the domain's own read tool).")
    L.append("")
    L.append(f"- **Date / stack / agent-model:** {meta.get('date','?')} · "
             f"{meta.get('gateway','?')} · `{meta.get('model_ref','?')}` (gemma, $0 local)")
    L.append(f"- **Auth:** {meta.get('auth_mode','?')} · **Fixture book:** "
             f"`{meta.get('fixture_book_id','?')}`")
    npass = sum(1 for r in rows if r["status"] == "PASS")
    nred = sum(1 for r in rows if r["status"] == "RED")
    L.append(f"- **Result:** {npass}/{len(rows)} PASS · {nred} RED · "
             f"{sum(1 for r in rows if r['status'] not in ('PASS','RED'))} other")
    L.append("")
    # RED rows first
    rows_sorted = sorted(rows, key=lambda r: (r["status"] != "RED", r.get("id", "")))
    L.append("| # | tool | service | tier | async | G1 sel | G2 shape | G3 exec | G4 effect | status |")
    L.append("|---|---|---|---|---|---|---|---|---|---|")
    for r in rows_sorted:
        L.append(
            f"| {r.get('id','')} | `{r['tool']}` | {r.get('service','')} | "
            f"{r.get('tier','?')} | {'yes' if r.get('async') else '-'} | "
            f"{_cell(r.get('G1'))} | {_cell(r.get('G2'))} | {_cell(r.get('G3'))} | "
            f"{_cell(r.get('G4'))} | **{r['status']}** |")
    L.append("")
    L.append("## Per-probe detail")
    L.append("")
    for r in rows_sorted:
        L.append(f"### {r.get('id','')} · `{r['tool']}` — {r['status']}")
        L.append(f"- **probe (NL):** {r.get('probe','')}")
        L.append(f"- **tier/async/paid:** {r.get('tier','?')} / {r.get('async')} / {r.get('paid')}")
        if r.get("notes"):
            L.append(f"- **notes:** {r['notes']}")
        ev = r.get("evidence") or {}
        if ev.get("call"):
            c = ev["call"]
            L.append(f"- **call:** args=`{json.dumps(c.get('args'), ensure_ascii=False)[:200]}` "
                     f"ok={c.get('ok')} err={str(c.get('error'))[:120]}")
        if ev.get("confirm"):
            L.append(f"- **confirm:** {json.dumps(ev['confirm'], ensure_ascii=False)[:200]}")
        if ev.get("readback"):
            L.append(f"- **effect read-back (independent path):** "
                     f"`{json.dumps(ev['readback'], ensure_ascii=False)[:300]}`")
        L.append("")
    return "\n".join(L)
